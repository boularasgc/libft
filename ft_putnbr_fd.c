#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	a;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	if (n > -1 && n < 10 && n != -2147483648)
	{
		a = n + '0';
		write(fd, &a, 1);
	}
	else if (n != -2147483648 && n > -1)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	if (n < 0 && n != -2147483648)
	{
		write(fd, "-", 1);
		ft_putnbr_fd(n * -1, fd);
	}
}
